export const TOP_10_MINISTERS = [...TELANGANA_MINISTERS]
  .sort((a, b) => b.activityScore - a.activityScore)
  .slice(0, 10);

export const getMinisterById = (id) =>
  TELANGANA_MINISTERS.find((m) => m.id === id) || null;

export const getMinisterByConstituency = (constituency) =>
  TELANGANA_MINISTERS.find(
    (m) => m.constituency.toLowerCase() === constituency.toLowerCase()
  ) || null;

export const getMinisterInitials = (name) => {
  const parts = name.trim().split(' ');
  if (parts.length === 1) return parts[0][0].toUpperCase();
  return (parts[0][0] + parts[parts.length - 1][0]).toUpperCase();
};
